let estacao = 1

switch (estacao) {
    case 1:
        console.log("Verão");
        break;
    case 2:
        console.log("Outono");
        break;
    case 3:
        console.log("Inverno");
        break;
    case 4:
        console.log("Primavera");
        break;
    default:
        console.log("Estação inválida");
}
